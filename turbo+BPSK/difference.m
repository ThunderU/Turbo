function [data_diff] = difference(data)
%差分编码
%**************************************************************************
%data           输入信号
%data_diff      差分编码后信号
%**************************************************************************

%--------------------------------------------------------------------------
data_diff = zeros(1,length(data));
data_diff(1) = 1 * data(1);   %1为差分编码的初始参考值
for i = 2:length(data)
    data_diff(i) = data_diff(i-1) * data(i);
end
%**************************************************************************